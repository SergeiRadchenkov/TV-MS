'''
Посчитать коэффициент линейной регрессии при заработной плате (zp), 
используя градиентный спуск (без intercept).
'''
import numpy as np

# Данные
zp = np.array([35, 45, 190, 200, 40, 70, 54, 150, 120, 110])
ks = np.array([401, 574, 874, 919, 459, 739, 653, 902, 746, 832])

# Параметры градиентного спуска
learning_rate = 0.000001
n_iterations = 10000

# Инициализация коэффициента
b = 0

# Градиентный спуск
for _ in range(n_iterations):
    gradient = -2 * np.sum(zp * (ks - b * zp)) / len(zp)
    b -= learning_rate * gradient

print(f'Коэффициент линейной регрессии (градиентный спуск, без intercept): b = {b}')

# Коэффициент линейной регрессии (градиентный спуск, без intercept): 
# b = 5.889820420132673